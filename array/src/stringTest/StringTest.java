package stringTest;

public class StringTest {
	public static void main(String[] args) {
	//문자열 문자배열이다.
      String data = "ABC";
      System.out.println(data.length()); //길이 찾기
      System.out.println(data.charAt(0)); //0번째에 있는 문자 찾기
      System.out.println(data.indexOf("B"));//B가 있는 인덱스 찾기		
	}
}
